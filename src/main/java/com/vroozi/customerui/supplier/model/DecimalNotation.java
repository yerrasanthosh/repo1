public class DecimalNotation {
    private int decimalId;
    private String decimalNotationName;
    private String decimalNotationValue;

    public int getDecimalId() {
        return decimalId;
    }

    public void setDecimalId(int decimalId) {
        this.decimalId = decimalId;
    }

    public String getDecimalNotationName() {
        return decimalNotationName;
    }

    public void setDecimalNotationName(String decimalNotationName) {
        this.decimalNotationName = decimalNotationName;
    }

    public String getDecimalNotationValue() {
        return decimalNotationValue;
    }

    public void setDecimalNotationValue(String decimalNotationValue) {
        this.decimalNotationValue = decimalNotationValue;
    }
}
